package ac.uk.vtate.lab3;

public class Increment {

	public static void main(String[] args) {
		
		int score = 80;
		
		System.out.println("Initial score: " + score);
		
		
		// increment the score
		score++;
		System.out.println("Initial score: " + score);
		
		
		// decrement the score
		score--;
		System.out.println("Initial score: " + score);

	}

}
