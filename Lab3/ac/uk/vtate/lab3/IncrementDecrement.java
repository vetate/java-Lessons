public class IncrementDecrement
{
   public static void main(String[] arg)
   {
      int x = 4, y = 4;

      System.out.println ("x =" + ++x);
      System.out.println ("x =" + y++);
      System.out.println ("x =" + x);
      System.out.println ("x =" + y);
   }//main
}//class
