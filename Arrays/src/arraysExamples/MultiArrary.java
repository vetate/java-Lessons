package arraysExamples;

public class MultiArrary {

	public static void main(String[] args) {
		
		// Declare and initialise a 2D array
		int[][] grid = {
		  {1, 2, 3},
		  {4, 5, 6},
		  {7, 8, 9}
		};

		// Accessing and printing an element from the 2D array
		int value = grid[1][2];
		System.out.println("Value: " + value);


	}

}
