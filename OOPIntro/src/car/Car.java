package car;

//Car.java - Define the Car class representing a basic car
class Car {
	// Attributes (data)
	String make;
	String model;
	int year;

	// (methods)
	void startEngine() {
		System.out.println("Engine started!");
	}

	void accelerate() {
		System.out.println("Car is accelerating!");
	}

	void brake() {
		System.out.println("Car is braking!");
	}
}
